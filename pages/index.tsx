import axios from 'axios';
import type { NextPage } from 'next'
import Head from 'next/head'
import { useEffect, useState } from 'react';

const Home: NextPage = () => {
  const [recommendedProducts, setRecommendedProducts] = useState([]);

  useEffect(() => {

    const fetchRecommendedProducts = async() =>{
      try {
        const response = await axios.get('http://127.0.0.1:5000/api/recommended-products');
        setRecommendedProducts(response.data);
      } catch (error) {
        console.log(error);
      }
    }

    fetchRecommendedProducts();
  
  }, []);
  return (
    <div>
      <Head>
        <title>ProductRecommendation</title>
        <meta name="Personalized Product Recommendation System" content="Generated by create next app" />
      </Head>
      
      {
        recommendedProducts.length ?
        recommendedProducts.map((product: any) => <h1>{product?.name}</h1>)
        :
        <h1>No Products</h1>
      }

    </div>
  )
}

export default Home
